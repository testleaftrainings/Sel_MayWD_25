package week1.day1;

public class LearnOperators {

	public static void main(String[] args) {

		System.out.println(5+8);//13
		System.out.println(2*4);//8
		System.out.println(8>9); //false
		System.out.println(2<0); //false
		System.out.println(6==5); // false
		System.out.println(8!=5);//true
		System.out.println((8>=7)+"\n"+(4+6)); //true
	}

}
